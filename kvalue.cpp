#include <cassert>
#include "kvalue.h"
#include "kvm.h"

using std::string;
using std::unordered_map;

///////////////////////////////////////////////////////////////////////////////
const Value* car(const Value *v)
{
    assert(v->type() == ValueType::CELL);
    return v->cell[0];
}
///////////////////////////////////////////////////////////////////////////////
const Value* cdr(const Value *v)
{
    assert(v->type() == ValueType::CELL);
    return v->cell[1];
}

const Value* cadr(const Value *v)
{
    return car(cdr(v));
}
